public class HappyBirthday 
{
	public static void main(String args[])
	{
		
		int count;
		count = 1;
		
		while (count <=100)    // For loop starts at 1 and ends at 100
		{
			System.out.println("Happy birthday!");
			count++;
		}
	  
	}
}